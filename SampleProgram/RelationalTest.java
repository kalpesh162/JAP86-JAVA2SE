class RelationalTest{
	public static void main(String[] args) {
		
		int n1=11;
		int n2=11;

		System.out.println("<   "+(n1<n2));
		System.out.println(">   "+(n1>n2));
		System.out.println("<=  "+(n1<=n2));
		System.out.println(">=  "+(n1>=n2));
		System.out.println("!=  "+(n1!=n2));
		System.out.println("==  "+(n1==n2));
	}
}